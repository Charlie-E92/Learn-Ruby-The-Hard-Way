# The variable is assigned to a tabbed in string.
tabby_cat = "\tI'm tabbed in."
# The variable is assigned to a string split over two lines.
persian_cat = "I'm split\non a line."
# \\ Is an escape sequence for a single backslash.
backslash_cat = "I'm \\ a \\ cat."
# ''' is used for multi-line comments
fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

puts tabby_cat
puts persian_cat
puts backslash_cat
puts fat_cat
